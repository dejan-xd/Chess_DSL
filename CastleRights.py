class CastleRights:
    def __init__(self, wks, bks, wqs, bqs):
        """
        Return True or False for castling rights.
        :param wks: white king side
        :param bks: black king side
        :param wqs: white queen side
        :param bqs: black queen side
        """
        self.wks = wks
        self.bks = bks
        self.wqs = wqs
        self.bqs = bqs
